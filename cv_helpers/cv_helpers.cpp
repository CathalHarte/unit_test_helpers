void show_impl(cv::Mat curiosity, std::string name)
{
    cv::namedWindow(name, cv::WINDOW_NORMAL);
    cv::resizeWindow(name, 800, 800);
    cv::imshow(name, curiosity);
    cv::waitKey(0);
}

